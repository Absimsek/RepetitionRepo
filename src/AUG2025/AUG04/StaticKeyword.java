public class StaticKeyword {
    // static keyword makes a variable or method to be seen from anywhere(it is common element for all objects)
    // so that coder doesn't need any object reference to access it and can access it by just class reference
    // static members are created just once and never need to create it again since it is common for all instances of class

public static String staticStr="this is a static string";
public String str="this is a non-static string";

}
